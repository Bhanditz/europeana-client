package eu.europeana.api.client.myeuropeana.result;

public class TagItem {

	String id = null;
	String europeanaId = null;
	String guid = null;
	String link = null;
	String title = null;
	String edmPreview = null;
	String type = null;
	String dateSaved = null;
	String tag = null;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getEuropeanaId() {
		return europeanaId;
	}
	public void setEuropeanaId(String europeanaId) {
		this.europeanaId = europeanaId;
	}
	public String getGuid() {
		return guid;
	}
	public void setGuid(String guid) {
		this.guid = guid;
	}
	public String getLink() {
		return link;
	}
	public void setLink(String link) {
		this.link = link;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getEdmPreview() {
		return edmPreview;
	}
	public void setEdmPreview(String edmPreview) {
		this.edmPreview = edmPreview;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getDateSaved() {
		return dateSaved;
	}
	public void setDateSaved(String dateSaved) {
		this.dateSaved = dateSaved;
	}
	public String getTag() {
		return tag;
	}
	public void setTag(String tag) {
		this.tag = tag;
	}
	
}
